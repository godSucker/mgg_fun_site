function getGachaMeta(id) {
  const definition = gachaMap[id];
  if (!definition) {
    return null;
  }
  const totalOdds = definition.basic_elements.reduce((sum, item) => sum + item.odds, 0);
  const name = GACHA_NAME_RU[id] ?? id;
  const cover = GACHA_COVERS[id] ?? null;
  return {
    id,
    name,
    totalOdds,
    cover,
    ...definition
  };
}
function listGachas() {
  return Object.keys(gachaMap).map((id) => getGachaMeta(id)).filter(Boolean).sort((a, b) => a.name.localeCompare(b.name, "ru"));
}
function getMutantName(specimenId) {
  return mutantNames[specimenId] ?? specimenId;
}

